"""Generate Agent Kali profile.md for each participant.

Gathers data from:
1. LightRAG (ndis workspace) — personal details, NDIS info, diagnosis
2. Local SC folder — reads all PDFs, DOCXs, MDs in the participant's folder
3. Compiles into a structured markdown profile

Usage:
    cd python && .venv/bin/python generate_profiles.py
"""

